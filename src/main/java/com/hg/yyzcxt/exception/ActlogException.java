package com.hg.yyzcxt.exception;

/**
 *   异常
 * @author 
 *
 */
public class ActlogException extends Exception {

	public ActlogException() {
	}

	public ActlogException(String message) {
		super(message);
	}

	public ActlogException(Throwable cause) {
		super(cause);
	}

	public ActlogException(String message, Throwable cause) {
		super(message, cause);
	}

}
