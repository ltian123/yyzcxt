package com.hg.yyzcxt.exception;

/**
 *   异常
 * @author 
 *
 */
public class KindDetailException extends Exception {

	public KindDetailException() {
	}

	public KindDetailException(String message) {
		super(message);
	}

	public KindDetailException(Throwable cause) {
		super(cause);
	}

	public KindDetailException(String message, Throwable cause) {
		super(message, cause);
	}

}
